# Write a program that asks the user to enter two numbers and compares them using the following comparison operators: 
# >`, `<`, `>=`, `<=`, `==`, and `!=`. 
# The program should print out the result of each comparison.
num1=int(input('Enter the first number:'))
num2=int(input('Enter the second number:'))

print(num1>num2) # performs the greater than operation
print(num1<num2) # performs the less than operation
print(num1>=num2) # performs the graeter than or equal to operation
print(num1<=num2) # performs the less than oequal to operation 
print(num1==num2) # performs the equal to operation
print(num1!=num2) # performs the not equal to operation
